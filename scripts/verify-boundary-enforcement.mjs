#!/usr/bin/env node
// Proves the Clean Architecture dependency rule (M2 design) is actually
// enforced by tooling, not just documented. Two fixture files are written
// into packages/domain/src (always cleaned up in `finally`, even on Ctrl-C
// via the SIGINT/SIGTERM handlers below):
//
//   1. A BARE-SPECIFIER import of @careerpilot/infrastructure. This must
//      fail `tsc` with TS2307 (module not found) — pnpm only links a
//      package's own declared dependencies into its node_modules, and
//      domain/package.json declares none (ADR-001).
//   2. A RELATIVE-PATH import reaching across package folders on disk
//      (`../../infrastructure/src/...`). This SUCCEEDS under tsc — relative
//      imports bypass node_modules resolution entirely — which is exactly
//      why eslint.config.js's `import/no-relative-packages: error` rule
//      exists as the second enforcement layer. This must fail ESLint.
//
// If either fixture does NOT fail the way it's supposed to, the boundary
// is not actually enforced and this script exits 1 — failing CI.

import { writeFileSync, rmSync, existsSync } from 'node:fs';
import { execFileSync } from 'node:child_process';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.dirname(fileURLToPath(import.meta.url)) + '/..';
const bareFixture = path.join(repoRoot, 'packages/domain/src/__boundary_fixture_bare__.ts');
const relativeFixture = path.join(repoRoot, 'packages/domain/src/__boundary_fixture_relative__.ts');

function cleanup() {
  for (const f of [bareFixture, relativeFixture]) {
    if (existsSync(f)) rmSync(f);
  }
}

function run(cmd, args) {
  try {
    execFileSync(cmd, args, { cwd: repoRoot, stdio: 'pipe' });
    return { ok: true };
  } catch (err) {
    return { ok: false, output: `${err.stdout ?? ''}${err.stderr ?? ''}` };
  }
}

process.on('SIGINT', () => { cleanup(); process.exit(130); });
process.on('SIGTERM', () => { cleanup(); process.exit(143); });

let failures = 0;

try {
  // --- Fixture 1: bare-specifier import, must fail tsc (TS2307) ---
  writeFileSync(
    bareFixture,
    "// AUTO-GENERATED by scripts/verify-boundary-enforcement.mjs — should never be committed.\n" +
    "export { createDb } from '@careerpilot/infrastructure';\n",
  );
  const tscBare = run('node_modules/.bin/tsc', ['--noEmit', '-p', 'packages/domain/tsconfig.json']);
  if (tscBare.ok) {
    console.error('BOUNDARY VIOLATION NOT CAUGHT: a bare-specifier import of @careerpilot/infrastructure ' +
      'from packages/domain compiled successfully. pnpm\'s isolated node_modules should make this TS2307.');
    failures++;
  } else {
    console.log('OK: bare-specifier cross-boundary import correctly fails tsc (TS2307).');
  }
  rmSync(bareFixture);

  // --- Fixture 2: relative-path import, must fail eslint (import/no-relative-packages) ---
  writeFileSync(
    relativeFixture,
    "// AUTO-GENERATED by scripts/verify-boundary-enforcement.mjs — should never be committed.\n" +
    "export { createDb } from '../../infrastructure/src/db/client.js';\n",
  );
  const eslintRelative = run('node_modules/.bin/eslint', [relativeFixture]);
  if (eslintRelative.ok) {
    console.error('BOUNDARY VIOLATION NOT CAUGHT: a relative-path import reaching from packages/domain into ' +
      'packages/infrastructure passed ESLint. import/no-relative-packages should have failed it.');
    failures++;
  } else {
    console.log('OK: relative-path cross-boundary import correctly fails eslint (import/no-relative-packages).');
  }
  rmSync(relativeFixture);
} finally {
  cleanup();
}

if (failures > 0) {
  console.error(`\n${failures} boundary-enforcement check(s) failed to catch a violation.`);
  process.exit(1);
}
console.log('\nBoth Clean Architecture boundary enforcement layers (tsc + eslint) verified.');
